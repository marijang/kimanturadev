
<h4>Politika privatnosti i Politika kolačića</h4>

<p><strong>Kimnatura d.o.o.</strong> posvećuje veliku važnost zaštiti osobnih podataka naših korisnika. Politika zaštite privatnosti uređuje način postupanja s informacijama koje Kimnatura d.o.o. obrađuje odnosno prikuplja prilikom posjeta našoj internet stranici. Napominjemo kako se obrada prikupljenih osobnih podataka temelji na jednoj od zakonitih osnova obrade u skladu s Općom uredbom o zaštiti podataka. Politika zaštite privatnosti obuhvaća:</p>

<ul class="browser-default">
    <li>Korištenje i otkrivanje odnosno prijenos osobnih podataka,</li>
    <li>Politiku vezanu uz korištenje i pohranjivanje Kolačića (HTTP cookies),</li>
    <li>pravila vezana uz Newsletter i pretplatu,</li>
    <li>Sigurnost,</li>
    <li>Rokove pohranjivanja,</li>
    <li>Prava korisnika,</li>
    <li>Automatiziranu obradu,</li>
    <li>Veze s drugim stranicama te Obavijest o promjenama i kontakt.</li>
</ul>

